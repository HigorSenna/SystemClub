     private void preencheGrid(ArrayList<Dependente> listaD) {
        DefaultTableModel table = (DefaultTableModel) jTabelaDep.getModel(); /*DfaultTableModel 
        utilizado para nao ter que escrever toda hora jTabela.getModel().removeRow,
        jTabela.getModel().addRow(linha) etc*/

        for (int i = 0; i < table.getRowCount(); i++) {
            table.removeRow(i);
        }

        for (Dependente dep : listaD) {
            String[] linha = {dep.getCPF(),
                dep.getNome(),
                dep.getEndereco(),
                dep.getTelefone(),
                dep.getRG(),
                dep.getSenhaClube()};
            table.addRow(linha);
        }        
        
    }

