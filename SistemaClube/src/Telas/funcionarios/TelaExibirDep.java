    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTabelaDep.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um Dependente");
        } else {
            if (JOptionPane.showConfirmDialog(this, "Deseja Apagar o Dependente "
                    + jTabelaDep.getValueAt(jTabelaDep.getSelectedRow(), 1) + "?", "Apagar",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                Dependente dep = new Dependente();
                dep.setCPF((String) jTabelaDep.getValueAt(jTabelaDep.getSelectedRow(),0));

                try {

                    new DependenteDao().deletar(dep);       
                      //preencher a grid
                    
                    ArrayList<Dependente> listaD = new DependenteDao().buscarTodos();
                    listaD.removeAll(listaD);

                    preencheGrid(listaD);
                   

                } catch (ClassNotFoundException ex) {
                    JOptionPane.showMessageDialog(this, "Contate o Desenvolvedor");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "Contate o Desenvolvedor " + ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

